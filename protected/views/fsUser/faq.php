			<div class="box-question-inner">What if I don't want to take a picture?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			Fire Risk Analysts at Wildfire Defense Systems view your photos to assess wildfire risks and create a personalized wildfire protection plan for you.   The more photos you provide, the more information the Fire Risk Analysts have to create your final report recommendation.  We encourage you to take as many photos as you think will help illustrate the property and surroundings.
