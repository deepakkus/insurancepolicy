		<div class="box-question">
			<div class="box-question-inner">When I'm taking a picture, what are the blue lines or blue boxes for?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			The blue lines are photo guides designed to help easily capture a photo of the specific condition.  This will ensure that the photo viewed by the Fire Risk Analyst will clearly display the specific wildfire risk.
		</div>
	</div>
	<div class="box1">
		<div class="box-question">
			<div class="box-question-inner">How many pictures should I take?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
