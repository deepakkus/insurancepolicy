	<div class="box1">
		<div class="box-question">
			<div class="box-question-inner">Do I always have to take a picture when asked?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			You are not required to take a picture for any condition.  If you don't want to include a photo, simply hit 'Done' on the photo-taking screen and you will be taken back to the question screen; pressing the 'Next' button will also allow you to bypass any photo-taking.
		</div>
	</div>
