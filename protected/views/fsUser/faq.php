	<div class="box1">
		<div class="box-question">
			<div class="box-question-inner">What is Wildfire Defense Systems?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			Wildfire Defense Systems (WDS) is an industry leader in wildfire education and mitigation strategies, with staff that includes Fire Science Professors and experts from all areas of the wildfire industry, including certified wildland firefighters with thousands of training hours and extensive experience battling wildfires.
		</div>
	</div>
	<div class="box1">
		<div class="box-question">
			<div class="box-question-inner">Why was I selected?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			Your invitation to use the WDSpro application (app) was based on wildfire risks near your property such as wildland vegetation type, vegetation density, topographic features, and wildland fire history.  Burning embers can travel over five miles, thus homes can still be threatened even when they seem to be located far from wildland fire risks.
		</div>
	</div>
	<div class="box1">
		<div class="box-question">
			<div class="box-question-inner">Why should I use this app to complete an assessment?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			The app provides personalized recommendations on how you can better protect your home from wildfires.
		</div>
	</div>
