			<div class="box-question-inner">Can I complete this assessment indoors?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			No, you will be asked to take exterior photos of your property. Just follow the instructions in the WDSpro app and take a short walk outside your home to capture key information about your property and surroundings.  If you don't know the answer to a question, that's ok. Just provide as much information as you can.  Please don't climb or stand on any objects to gather this information.<br/><br/>
            When you're ready to begin, pick a time in daylight hours, tap "Start New Assessment," and take your guided walk.  In a few days, you will receive a notification on your mobile device with your customized recommendations from the experts at Wildfire Defense Systems.
