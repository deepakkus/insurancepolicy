		<div class="box-question">
			<div class="box-question-inner">What if my address isn't showing?</div>
			<div class="expandButton">+</div>		
		</div>
		<div class="box-answer">
			Most members with an applicable USAA homeowners or rental property insurance policy for a home located in one of the 12 eligible states (AZ, CA, CO, ID, MT, NM, NV, OR, TX, UT, WA and WY) are eligible to participate in this program.  Condos, townhomes, cooperatives, apartments and mobile homes are not eligible for this program.  If your property meets these criteria and you do not see your address listed, please send an email to <a href="mailto:wildfiredefense@usaa.com">wildfiredefense@usaa.com</a> and include your USAA membership number and the property address.  You will receive a response within 1-2 business days.
		</div>
