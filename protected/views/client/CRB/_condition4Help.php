        <h3>Simple Vents</h3>
		<b>Select YES if any vent to the structure (gable, eave/soffit, foundation, weep gap/hole) is inadequately covered: no screen is present or screen is of gauge greater than 1/8-inch, damaged or insecurely attached.</b>
		<p>Vents on vertical walls or surfaces have been shown to be vulnerable to the entry of embers. An accumulation of embers within the attic, crawl space, or other vented space can ignite combustible materials within the space. All make-up air and moisture ventilation should be screened with 1/8 inch corrosion resistant metal mesh.</p>
