        <h3>Mechanical Vents</h3>
		<b>Select YES if any mechanical vent (dryer, kitchen or bathroom exhaust vents) on the structure does not have screening or lacks a fully functioning metal flap closure.</b>
		<p>Mechanical vents, such as dryer, kitchen, or bathroom exhaust vents, should not be screened as the screen will accumulate lint and debris--additional fuels on the home. Plastic vent covers can melt from exposure to radiant heat or flames.</p>
