        <h3>Combustible Roof</h3>
		<b>Select YES if the roof is wood shake or any other known combustible material, regardless of condition.</b>
		<p>The roof is the most vulnerable component of the home. The sloped surface will allow for the accumulation of debris, including potential burning embers. Combustible roofing, such as wood shake, will ignite from the collection of burning embers on its surface.</p> 
