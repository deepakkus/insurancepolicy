        <h3>Eaves</h3>
		<b>Select YES if the eaves are in fair to poor condition: the rafter tails, fascia, and/or soffit boards are damaged, splintering, or rotting or there are gaps greater than 1/4-inch in width between the boards, or combustible materials, such as plant debris or animal nests, are present within the eaves.</b>
		<p>Weathering, aging, and damage can render the eaves vulnerable to ignition. Gaps and/or large cracks in the eave components can provide openings to the structure interior and nooks for fine fuels to accumulate. The presence of debris or animal nests within the eaves provide fuel for wind-blown embers.</p>
