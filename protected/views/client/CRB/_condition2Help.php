        <h3>Roof Assembly</h3>
		<b>Select YES if the roof has missing shingles, gaps/holes at the eave ends (common in tile roofing), or other condition issues that make it vulnerable to ember intrusion.</b>

		<p>Even noncombustible and Class-A fire-resistant roofing can fail if there are vulnerabilities to its assembly. Inherent gaps, such as those present at the hips and eave ends of barrel tile or corrugated metal roofing, or damage to the roofing can allow embers access to the combustible underlayment of the roof. Look for clay or concrete barrel tiles that are not sealed along the roof ridges or do not have "bird stops" present at the eave ends. Examine the roof from all angles, looking for curled, damaged, or missing tiles or shingles.</p>

